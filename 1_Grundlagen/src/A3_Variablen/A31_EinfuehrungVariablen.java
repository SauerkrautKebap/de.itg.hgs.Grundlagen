package A3_Variablen;

public class A31_EinfuehrungVariablen {

	public static void main(String[] args) {

		//EINGABE
		//Deklaration 
		double zahl2;   //Kommazahl IEEE754 64Bit 
		double zahl3;
		double summe;
		
		
		//Initialisierung, Wertzuweisung
		zahl2 = -3.4;
		zahl3 = 5.5;
		
		
		//VERARBEITUNG
		summe = zahl2 + zahl3;
		
		
		
		//AUSGABE
		System.out.println(summe);
		
		
		
		
		
		
		
		
		
		
		

	}

}
